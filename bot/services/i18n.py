# Default fallback language
DEFAULT_LANG = "uz"

# For languages not in our translations dict, fall back to English
def get_trans(lang: str, key: str, **kwargs) -> str:
    """Get a translated string for the given language and key."""
    lang_data = TRANSLATIONS.get(lang) or TRANSLATIONS.get("en") or TRANSLATIONS.get(DEFAULT_LANG, {})
    text = lang_data.get(key, TRANSLATIONS.get("en", {}).get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


# Build reverse lookup: text → key (for detecting button presses in any language)
def build_text_to_key_map() -> dict:
    mapping = {}
    for lang_data in TRANSLATIONS.values():
        for key, text in lang_data.items():
            if key.startswith("btn_"):
                mapping[text] = key
    return mapping


TEXT_TO_KEY = build_text_to_key_map()


def get_button_key(text: str) -> str | None:
    """Given a button text in ANY language, return its key (e.g. 'btn_grammar')."""
    return TEXT_TO_KEY.get(text)
